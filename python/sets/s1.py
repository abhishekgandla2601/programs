# creating a set in python

set1 = {1,2,3,4}
print(set1)

# using set() function

set1 = set()
print(set1)

# creating a set with the use of a List

set1 = set(["abhi","loves","music"])
print(set1)

#creating a set with the use of a tuple
tup = ("abhi","loves","music")
print(tup)
print(set(tup))

# creating a set with the use of a dictionary
d = {"aaa":1, 'me':2, "aaa":2}
print(set(d))