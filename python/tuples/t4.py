# concatenation of tuples

tup1 = (1,2,3,4)
tup2 = ('Geeks', 'For', 'Geeks')

tup3 = tup1 + tup2

print(tup3)

# sicing of tuple

# [start, stop, step]

tup = tuple('abhishek')

# removing 1st element
print(tup[1:])

# reversing the tuple
print(tup[::1])

# print elements of a range

print(tup[4:8])

